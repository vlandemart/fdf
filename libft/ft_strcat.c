char	*ft_strcat(char *s1, const char *s2)
{
	char	*src;
	int		i;
	int		j;

	src = (char*)s2;
	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (src[j])
		s1[i++] = src[j++];
	s1[i] = '\0';
	return (s1);
}
